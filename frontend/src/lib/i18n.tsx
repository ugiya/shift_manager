const dict: Record<Lang, Record<Msg, string>> = { en, he };

// Short weekday names, Mon=0..Sun=6 (matching lookups.WEEKDAY_NAMES ordering).
export const HE_WEEKDAYS = ["ב׳", "ג׳", "ד׳", "ה׳", "ו׳", "ש׳", "א׳"];

export interface I18n {
  lang: Lang;
  setLang: (l: Lang) => void;
  /** t("key") or t("key", {n: 3}) — {placeholders} are replaced. */
  t: (k: Msg, vars?: Record<string, string | number>) => string;
  /** Weekday display names in the current language (Mon=0..Sun=6), or undefined for the default. */
  weekdayNames?: string[];
}

const Ctx = createContext<I18n>({ lang: "en", setLang: () => {}, t: (k) => en[k] });

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    try {
      return localStorage.getItem(KEY) === "he" ? "he" : "en";
    } catch {
      return "en";
    }
  });
  useEffect(() => {
    try {
      localStorage.setItem(KEY, lang);
    } catch { /* storage unavailable */ }
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === "he" ? "rtl" : "ltr";
  }, [lang]);
  const setLang = useCallback((l: Lang) => setLangState(l), []);
  const t = useCallback((k: Msg, vars?: Record<string, string | number>) => {
    let s: string = dict[lang][k] ?? en[k];
    if (vars) for (const [key, v] of Object.entries(vars)) s = s.replace(`{${key}}`, String(v));
    return s;
  }, [lang]);
  const value = useMemo<I18n>(
    () => ({ lang, setLang, t, weekdayNames: lang === "he" ? HE_WEEKDAYS : undefined }),
    [lang, setLang, t]);
  return <Ctx.Provider value={value}>{children}</Ctx.Provider>;
}

export function useI18n(): I18n {
  return useContext(Ctx);
}
